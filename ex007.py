# Exercício 007
''' Desenvolva um programa que leia duas notas de um aluno, calcule e mostre sua média. '''

# Solicitando as notas do aluno
nota_1 = float(input('Adicione 1° nota: '))
nota_2 = float(input('Adicione 2° nota: '))

# Cálculo da média do aluno
media = (nota_1 + nota_2) /2

# Exibindo notas do aluno e sua média final
print(f'Primeira nota: {nota_1}')
print(f'Segunda nota: {nota_2}')
print(f'Média final: {media}')
